import UIKit

struct ColorConstant {
    
    struct BlueColor {
        //bright to dark
        static let uranianBlue: UIColor = UIColor(named: "uranian_blue")!
        static let argentinianBlue: UIColor = UIColor(named: "argentinian_blue")!
        static let azureBlue: UIColor = UIColor(named: "azure")!
        static let indigoDyeBlue: UIColor = UIColor(named: "indigo_dye")!
    }
    
    struct RedColor {
        static let redRed: UIColor = UIColor(named: "red")!
    }
    
    struct BrownColor {
        static let sunsetBrown: UIColor = UIColor(named: "sunset")!
        static let ochreBrown: UIColor = UIColor(named: "ochre")!
    }
}
